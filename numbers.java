import java.util.*;

public class numbers {
    public static void main(String[] args) {

        @SuppressWarnings("resource")
        Scanner sc = new Scanner(System.in);
        int n = sc.nextInt();
        // sc.nextLine();
        for (int i = 0; i <= n; i++) {
            System.out.print(i + " ");
        }

    }

}
